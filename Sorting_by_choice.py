"""
Сортировка выбором.
Максимальный объем вычислений O(n**2)
"""
m = [7, 5, -8, -47, 50, 0, 22, - 12, 5, -30, 27, -7, 10, 1]
n = len(m)
for i in range(0, n-1):
    mi = m[i]  # Хранит минимальное значение
    p = i  # Хранит индекс минимального значения
    for j in range(i+1, n):
        if mi > m[j]:
            mi = m[j]
            p = j
    if p != i:
        m[p], m[i] = m[i], m[p]
print(m)
