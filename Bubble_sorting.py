"""
Сортировка пузырьком (метод всплывающего пузырька).
Максимальный объем вычислений O(n**2)
"""

m = [7, 5, -8, -47, 50, 0, 22, - 12, 5, -30, 27, -7, 10, 1]
n = len(m)
while n > 0:
    for i in range(0, n - 1):
        if m[i] > m[i + 1]:
            m[i], m[i+1] = m[i+1], m[i]
    n -= 1
print(m)
