"""
Слияние двух упорядоченных списков.
Сложность алгоритма O(n).
"""

x = [-47, -30, -27, -12, -8, -8, -7, 0, 1, 5, 5, 7, 10, 22, 27, 50]
y = [-82, -36, -27, -18, -8, -6, -5, -4, -3, -2, -1, 8, 12, 25, 40]
z = []

N = len(x)
M = len(y)

i = 0
j = 0

while i < N and j < M:
    if x[i] < y[j]:
        z.append(x[i])
        i += 1
    else:
        z.append(y[j])
        j += 1
z += x[i:] + y[j:]
print(z)
